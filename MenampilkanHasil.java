package me.raihanpratama.belajarjava;

public class MenampilkanHasil {

    public static void main(String[] args) {
        int nilaiAkhir = 0;

        for (int i = 1; i <= 5; i++) {
            nilaiAkhir += i;
            System.out.println(nilaiAkhir);

        }

        System.out.println(nilaiAkhir);
    }
}
